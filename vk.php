 *
 * @param object $data
 *
 * @return array $user
 */
function getUser($data)
{
    $id = $data->object->user_id;
    $info = json_decode(file_get_contents(API_URL . "users.get?user_ids={$id}&v=" . API_VERSION . '&access_token=' . TOKEN));
    $name = $info->response[0]->first_name;

    $user = [
        'id' => $id,
        'name' => $name
    ];

    return $user;
}

