 *
 * @param array $user
 * @param string $message
 *
 * @return string
 */
function request($user, $message)
{
    $params = array(
        'message' => "{$message}",
        'user_id' => $user['id'],
        'access_token' => TOKEN,
        'v' => API_VERSION
    );

    $params = http_build_query($params);
    file_get_contents(API_URL . 'messages.send?' . $params);
    echo('ok');
}

